#include "ft_printf.h"

size_t	ft_strlen_p(const char *str)
{
	size_t	c;
	size_t	f;

	c = 0;
	f = 0;
	while (str[c] != '\0')
	{
		if (str[c] == '\\' && str[c + 1] == '0')
			f++;
		c++;
	}
	return (c - f);
}
