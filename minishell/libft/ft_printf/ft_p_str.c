#include "ft_printf.h"

void	ft_p_str(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if ((int)c[i] == 255)
		{
			write(1, "\0", 1);
		}
		else
			write(1, &c[i], 1);
		i++;
	}
}
